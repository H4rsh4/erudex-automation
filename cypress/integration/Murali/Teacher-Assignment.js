/// <reference types="Cypress" />
import Teacherobj from '../../support/pageObjects/Teacherobj'
const TeacherOBJ = new Teacherobj()
const Constvarbl = require('../../fixtures/ConstVarbl.json')
describe('My First Test Suite', function() 
{
    it('Signin', ()=>{
        cy.Signin(Constvarbl.TeacherUserName,
            Constvarbl.TeacherPassword);
     }); 
it('Create Assignment',function() {
    TeacherOBJ.getCurriculam()
        .click()
    TeacherOBJ.getCreateAssignment()
        .click({force: true})
    TeacherOBJ.getName()
        .type(Constvarbl.name)
    TeacherOBJ.getClass()
        .contains(Constvarbl.Class)
    TeacherOBJ.getSubject()
        .contains(Constvarbl.Subject)
    TeacherOBJ.getSubChapter()
        .contains(Constvarbl.Chapter)
    cy.wait(2000)    
    TeacherOBJ.getSubjectTopics()
        .contains(Constvarbl.Topic)
            .click({force: true})
    cy.wait(2000)        
    TeacherOBJ.getSubjectTopics()
        .contains(Constvarbl.SubTopic)
            .click({force: true})
    TeacherOBJ.getMarks()
        .contains(Constvarbl.Marks)
    TeacherOBJ.getAssignmentDescription()
        .type(Constvarbl.Description)
    TeacherOBJ.getCreateActivtyPush()
        .click({force: true})
    TeacherOBJ.getCheckbox()
        .check()
            .should('be.checked')
    TeacherOBJ.getPushCalender1()
        .click()
    TeacherOBJ.getDateandTime()
        .contains(Constvarbl.pushDate)
            .click()
    TeacherOBJ.getCalenderHour()
        .contains(Constvarbl.pushHour1)
            .click()
    TeacherOBJ.getCalenderMinute()
        .contains(Constvarbl.pushMin1)
            .click()
    TeacherOBJ.getPushCalender2()
        .click()
    TeacherOBJ.getDateandTime1()
        .contains(Constvarbl.pushDate)
            .click()
    TeacherOBJ.getCalenderHour1()
        .contains(Constvarbl.pushHour2)
            .click()
    TeacherOBJ.getCalenderMinute1()
        .contains(Constvarbl.pushMin2)
            .click()
    TeacherOBJ.getCancel()
        .click()     
})
it('View Assignment',function() {
    TeacherOBJ.getViewAssignment()
        .click({force: true})
    TeacherOBJ.getClass()
        .contains(Constvarbl.Class)
    TeacherOBJ.getSubject()
        .contains(Constvarbl.Subject)
    TeacherOBJ.getSubChapter()
        .contains(Constvarbl.Chapter)
    TeacherOBJ.getdropdwnCreatedDate()
        .contains(Constvarbl.CreatedDate)
    TeacherOBJ.getCalender1()
        .click()
    TeacherOBJ.getDate()
        .contains(Constvarbl.Date1).click()    
    TeacherOBJ.getCalender2()
        .click()
    TeacherOBJ.getDate()
        .contains(Constvarbl.Date2).click()
    TeacherOBJ.getPushedTabButton()
        .click()
    TeacherOBJ.getAssignmentPushedTable().each(($e1,index,$list)=>{
        const text = $e1.text()
        if(text.includes("Mathematics"))
        {
            $e1.click()
        }
    })
    TeacherOBJ.getBack()
        .click()
    cy.Logout()
})
})